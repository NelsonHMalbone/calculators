# finding the area of a rectangle
# have user input the width
# have user input the height

# have users input a width and height
width = float(input("Enter a Width: "))
height = float(input("Enter a height: "))

area = width * height

print(f'You enter the width of {width} and the height of {height} which will give'
      f' you the area of {area}')
